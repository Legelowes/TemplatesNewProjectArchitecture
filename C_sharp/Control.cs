    // GET
  
    // SET
        private void setThread_coreId(int coreId)
        {
            this.thread_CoreId[coreId] = coreId;
            System.Console.WriteLine(">> >> >> SET Field Value : Static_XxxxxxXxxxxxx/Control_Execute/thread_CoreId[" + coreId + "] = " + coreId);//SIMULATION
        }
